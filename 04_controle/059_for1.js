let contador = 1
while(contador <= 5) {
    console.log(`contador = ${contador}`)
    contador++
}
/*
contador = 1
contador = 2
contador = 3
contador = 4
contador = 5
*/

for(let i = 1; i <= 5; i++) {
    console.log(`i = ${i}`)
}
/*
i = 1
i = 2
i = 3
i = 4
i = 5
*/

const notas = [6.7, 7.4, 9.8, 8.1, 7.7]
for(let i = 0; i < notas.length; i++) {
    console.log(`nota = ${notas[i]}`)
}
/*
nota = 6.7
nota = 7.4
nota = 9.8
nota = 8.1
nota = 7.7
*/
